package Practice;

public class SwitchStatements {

	public static void main(String[] args) {
		int num = 5;
		switch(num) {
		    default:
			    System.out.println("IDK");
			case 1: 
				System.out.println("The number is 1");
				System.out.println("Hi");
				break;
			case 2: 
				System.out.println("The number is 2");
				break;
			

	};

}
}
